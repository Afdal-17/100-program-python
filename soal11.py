siswa = (input("Masukan nilai siswa: "))

if int(siswa) >= 90:
    print("Nilai siswa A")

elif int(siswa) >= 80:
    print("Nilai siswa B")

elif int(siswa) >= 70:
    print("Nilai siswa C")

elif int(siswa) >= 60:
    print("Nilai siswa D")

elif int(siswa) >= 50:
    print("Nilai siswa E")

else:
    print("Siswa tidak masuk nilai sama sekali")