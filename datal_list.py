# Membuat data list dengan array
# Elemen pertama itu dimulai dari 0,1,2....
buah = ["anggur","apel","nanas","mangga","jeruk","durian"]
print(buah[1])
# Cara merubah buah nanas menjadi melon
buah[2] = "Melon"
print(buah)
# Cara menambahkan semangka ke list buah
buah.append("semangka")
print(buah)
for data in buah:
    print(data)